# creamos una lista con tres elementos
lista = [1, 2, 3]
print(lista)

# Para agregar elementos a una lista se puede utilizar el método .append, este método solo acepta un argumento
lista.append(4)
print(lista)

# Para generar una copia de la lista se puede utilizar el metodo .copy
lista2 = lista.copy()
print(lista2)

# Para eliminar todos los elementos en una lista se puede usar el metodo  .clear
lista.clear()
print(lista)